#include "../push_swap.h"

int	tab_is_sorted(t_tab *tab)
{
	int	i;

	i = 1;
	while (i < tab->size)
	{
		if (tab->tab[i - 1] > tab->tab[i])
			return (0);
		i++;
	}
	return (1);
}
